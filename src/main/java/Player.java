public class Player {

    private int healthPoints;
    private int insightPoints;
    private int defencePoints;
    private int intelligencePoints;
    private int inspirationPoints;

    public Player(int healthPoints, int insightPoints, int defencePoints, int intelligencePoints, int inspirationPoints) {
        this.healthPoints = healthPoints;
        this.insightPoints = insightPoints;
        this.defencePoints = defencePoints;
        this.intelligencePoints = intelligencePoints;
        this.inspirationPoints = inspirationPoints;
    }


}
